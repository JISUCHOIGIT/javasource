package api;

public class MathEx1 {

	public static void main(String[] args) {
		double val = 90.7552;
		System.out.println("반올림 " + Math.round(val)); //round : 반올림
		System.out.println("올림 " + Math.ceil(val)); //ceil : 올림
		System.out.println("버림 " + Math.floor(val)); //floor : 버림
		
		System.out.println(Math.PI);

	}

}
